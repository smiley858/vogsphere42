int		ft_strncmp(char *str1, char *str2, unsigned int n)
{
	while (*str1 == *str2 && n > 0)
	{
		if (*str1 == 0 && *str2 == 0)
			return (0);
		str1++;
		str2++;
		n--;
	}
	return (*str1 - *str2);
}
